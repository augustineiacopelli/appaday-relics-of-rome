// On install: do nothing automatically — tiles are fetched on demand by the user
self.addEventListener('install', function(e) {
  self.skipWaiting();
});

self.addEventListener('activate', function(e) {
  e.waitUntil(
    caches.keys().then(function(keys) {
      return Promise.all(
        keys.filter(function(k) { return k !== CACHE_NAME; })
            .map(function(k) { return caches.delete(k); })
      );
    })
  );
  self.clients.claim();
});

// Fetch: serve tiles from cache when available, fall back to network
self.addEventListener('fetch', function(e) {
  var url = e.request.url;
  if (url.indexOf('basemaps.cartocdn.com') !== -1) {
    e.respondWith(
      caches.open(CACHE_NAME).then(function(cache) {
        return cache.match(e.request).then(function(cached) {
          if (cached) { return cached; }
          return fetch(e.request).then(function(response) {
            if (response && response.status === 200) {
              cache.put(e.request, response.clone());
            }
            return response;
          }).catch(function() {
            // offline and not cached — return transparent 1px tile
            return new Response(
              new Uint8Array([137,80,78,71,13,10,26,10,0,0,0,13,73,72,68,82,0,0,0,1,0,0,0,1,8,2,0,0,0,144,119,83,222,0,0,0,12,73,68,65,84,8,215,99,248,207,192,0,0,0,2,0,1,226,33,188,51,0,0,0,0,73,69,78,68,174,66,96,130]).buffer,
              { headers: { 'Content-Type': 'image/png' } }
            );
          });
        });
      })
    );
  }
});

// Message: trigger bulk tile pre-cache
self.addEventListener('message', function(e) {
  if (e.data && e.data.type === 'PRECACHE_TILES') {
    var total = ROME_TILES.length;
    var done = 0;
    var failed = 0;

    caches.open(CACHE_NAME).then(function(cache) {
      var batches = [];
      var batchSize = 20;
      for (var i = 0; i < ROME_TILES.length; i += batchSize) {
        batches.push(ROME_TILES.slice(i, i + batchSize));
      }

      function processBatch(idx) {
        if (idx >= batches.length) {
          e.source.postMessage({ type: 'PRECACHE_DONE', total: total, failed: failed });
          return;
        }
        var batch = batches[idx];
        var fetches = batch.map(function(url) {
          return cache.match(url).then(function(hit) {
            if (hit) { done++; return; }
            return fetch(url, { mode: 'no-cors' }).then(function(resp) {
              if (resp && (resp.status === 200 || resp.type === 'opaque')) {
                cache.put(url, resp);
              }
              done++;
            }).catch(function() { done++; failed++; });
          });
        });
        Promise.all(fetches).then(function() {
          e.source.postMessage({ type: 'PRECACHE_PROGRESS', done: done, total: total });
          processBatch(idx + 1);
        });
      }

      processBatch(0);
    });
  }
});
